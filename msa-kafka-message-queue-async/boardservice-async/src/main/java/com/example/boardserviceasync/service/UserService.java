package com.example.boardserviceasync.service;

import com.example.boardserviceasync.domain.User;
import com.example.boardserviceasync.dto.SaveUserRequestDto;
import com.example.boardserviceasync.repository.UserRepository;
import org.springframework.stereotype.Service;
import org.springframework.transaction.annotation.Transactional;

@Service
public class UserService {

  private final UserRepository userRepository;

  public UserService(UserRepository userRepository) {
    this.userRepository = userRepository;
  }

  @Transactional
  public void save(SaveUserRequestDto saveUserRequestDto) {
    User user = new User(
        saveUserRequestDto.getUserId(),
        saveUserRequestDto.getName()
    );

    this.userRepository.save(user);
  }
}
